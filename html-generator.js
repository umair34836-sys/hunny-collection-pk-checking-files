// Generate SEO meta tags HTML
function generateSEOMetaTags(seoData) {
  return `    <!-- SEO Meta Tags (Generated by SEO Dashboard - ${new Date().toLocaleDateString('en-PK')}) -->
    <title>${seoData.title}</title>
    <meta name="description" content="${seoData.description}">
    <meta name="keywords" content="${seoData.keywords}">
    <meta name="author" content="Hunny Collection PK">
    <meta name="robots" content="${seoData.robots}">
    <link rel="canonical" href="${seoData.canonical}">

    <!-- Open Graph / Facebook -->
    <meta property="og:type" content="website">
    <meta property="og:url" content="${seoData.canonical}">
    <meta property="og:title" content="${seoData.title}">
    <meta property="og:description" content="${seoData.description}">
    <meta property="og:image" content="${seoData.ogImage}">

    <!-- Twitter -->
    <meta property="twitter:card" content="summary_large_image">
    <meta property="twitter:title" content="${seoData.title}">
    <meta property="twitter:description" content="${seoData.description}">
    <meta property="twitter:image" content="${seoData.ogImage}">`;
}

// Fetch HTML file content
async function fetchHTMLFile(filename) {
  try {
    const response = await fetch(filename);
    if (!response.ok) {
      throw new Error(`Failed to fetch ${filename}`);
    }
    return await response.text();
  } catch (error) {
    console.error(`Error fetching ${filename}:`, error);
    throw error;
  }
}

// Update HTML with new SEO meta tags
function updateHTMLWithSEO(htmlContent, seoData, pageName) {
  const newMetaTags = generateSEOMetaTags(seoData);
  
  // Pattern to find existing SEO meta tags section
  const seoPattern = /<!-- SEO Meta Tags -->[\s\S]*?<\/head>/i;
  const titlePattern = /<title>[\s\S]*?<\/title>/i;
  const descriptionPattern = /<meta name="description" content="[^"]*">/i;
  
  let updatedHTML = htmlContent;
  
  // Check if SEO section exists
  if (seoPattern.test(htmlContent)) {
    // Replace existing SEO section
    updatedHTML = htmlContent.replace(seoPattern, newMetaTags + '\n</head>');
  } else {
    // Find </head> and insert before it
    const headClosePattern = /<\/head>/i;
    if (headClosePattern.test(htmlContent)) {
      // Insert new SEO tags before </head>
      updatedHTML = htmlContent.replace(headClosePattern, newMetaTags + '\n</head>');
    } else {
      // No </head> found, add to beginning
      updatedHTML = '<head>\n' + newMetaTags + '\n</head>\n' + htmlContent;
    }
  }
  
  // Add timestamp comment
  const timestampComment = `<!-- Last SEO Update: ${new Date().toLocaleString('en-PK')} -->\n`;
  updatedHTML = timestampComment + updatedHTML;
  
  return updatedHTML;
}

// Download HTML file
function downloadHTML(content, filename) {
  const blob = new Blob([content], { type: 'text/html' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = filename;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}

// Download single page HTML
export async function downloadPageHTML(pageName) {
  const seoData = SEO_DATA[pageName];
  const filename = PAGE_FILES[pageName];
  
  if (!seoData) {
    throw new Error(`No SEO data found for page: ${pageName}`);
  }
  
  if (!filename) {
    throw new Error(`No filename mapping for page: ${pageName}`);
  }
  
  try {
    // Fetch current HTML
    const htmlContent = await fetchHTMLFile(filename);
    
    // Update with SEO
    const updatedHTML = updateHTMLWithSEO(htmlContent, seoData, pageName);
    
    // Download
    downloadHTML(updatedHTML, filename);
    
    // Log the update
    logSEOUpdate(pageName, 'Downloaded HTML');
    
    return {
      success: true,
      message: `${filename} downloaded successfully!`,
      filename: filename
    };
  } catch (error) {
    console.error('Error downloading HTML:', error);
    return {
      success: false,
      message: `Error downloading ${filename}: ${error.message}`,
      error: error
    };
  }
}

// Download all HTML files
export async function downloadAllHTML() {
  const results = [];
  
  for (const [pageName, filename] of Object.entries(PAGE_FILES)) {
    try {
      const result = await downloadPageHTML(pageName);
      results.push(result);
      
      // Small delay between downloads
      await new Promise(resolve => setTimeout(resolve, 500));
    } catch (error) {
      results.push({
        page: pageName,
        success: false,
        message: error.message
      });
    }
  }
  
  return results;
}

// Download SEO config as JSON (for backup)
export function downloadSEOConfig() {
  const config = {
    version: '1.0',
    lastUpdated: new Date().toISOString(),
    seoData: SEO_DATA
  };
  
  const blob = new Blob([JSON.stringify(config, null, 2)], { type: 'application/json' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = 'seo-backup.json';
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
  
  return {
    success: true,
    message: 'SEO configuration downloaded as seo-backup.json'
  };
}

// Log SEO updates to localStorage
function logSEOUpdate(pageName, action) {
  const logs = JSON.parse(localStorage.getItem('seo_update_logs') || '[]');
  
  logs.unshift({
    page: pageName,
    pageName: PAGE_NAMES[pageName] || pageName,
    action: action,
    timestamp: new Date().toISOString(),
    timestampLocal: new Date().toLocaleString('en-PK')
  });
  
  // Keep only last 50 logs
  if (logs.length > 50) {
    logs.splice(50);
  }
  
  localStorage.setItem('seo_update_logs', JSON.stringify(logs));
}

// Get SEO update logs
export function getSEOLogs() {
  return JSON.parse(localStorage.getItem('seo_update_logs') || '[]');
}

// Clear SEO logs
export function clearSEOLogs() {
  localStorage.removeItem('seo_update_logs');
  return { success: true, message: 'Logs cleared' };
}

// Preview SEO changes before download
export function previewSEOChanges(pageName) {
  const seoData = SEO_DATA[pageName];
  
  if (!seoData) {
    return null;
  }
  
  return {
    page: PAGE_NAMES[pageName] || pageName,
    filename: PAGE_FILES[pageName],
    changes: {
      title: seoData.title,
      description: seoData.description,
      keywords: seoData.keywords,
      ogImage: seoData.ogImage,
      canonical: seoData.canonical,
      robots: seoData.robots
    },
    characterCounts: {
      title: seoData.title.length,
      description: seoData.description.length,
      keywords: seoData.keywords.length
    },
    recommendations: getCharacterRecommendations(seoData)
  };
}

// Get character count recommendations
function getCharacterRecommendations(seoData) {
  const recs = [];
  
  if (seoData.title.length < 30) {
    recs.push('⚠️ Title is too short (min 30 characters)');
  } else if (seoData.title.length > 60) {
    recs.push('⚠️ Title is too long (max 60 characters) - may be truncated');
  } else {
    recs.push('✅ Title length is optimal');
  }
  
  if (seoData.description.length < 50) {
    recs.push('⚠️ Description is too short (min 50 characters)');
  } else if (seoData.description.length > 160) {
    recs.push('⚠️ Description is too long (max 160 characters) - may be truncated');
  } else {
    recs.push('✅ Description length is optimal');
  }
  
  return recs;
}

